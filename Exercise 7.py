# first loop - 0 to 50 in increments of 1
for n in range(0, 51, 1):
    print(n, end=", ")
print("\n") # adds space between each counting instance

# second loop - 50 to 0 in decrements of 1
for n in range(50, -1, -1):
    print(n, end=", ")
print("\n")

# third loop - 30 to 50 in increments of 1
for n in range(30, 51, 1):
    print(n, end=", ")
print("\n")

# fourth loop - 50 to 10 in decrements of 2
for n in range(50, 9, -2):
    print(n, end=", ")
print("\n")

# fifth loop - 100 to 200 in increments of 5
for n in range(100, 201, 5):
    print(n, end=", ")
